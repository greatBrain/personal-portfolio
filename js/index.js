function closeWindow() {
    var window = document.getElementById("inner-skills-window");
    if (window){
        window.style.display = "none";
    }
}

function showWindow() {
    var window = document.getElementById("inner-skills-window"); // Replace "yourDivId" with the actual ID of your div
    if (window) {
        window.style.display = "block";
    }
}

function maximizeWindow() {
    var window = document.getElementById("inner-skills-window"); // Replace "yourDivId" with the actual ID of your div
    if (window) {
        window.style.display = "block";
    }
}